class Typo
{
	public static void main(String[] args)
	{
	//Widening Byte into Short
	byte a=2;
	short z=a;
	
	System.out.println("Short value:"+z);
	System.out.println("Byte value:"+z);
	
	//Byte into Int
	byte a1=5;
	int z1=a1;
	
	System.out.println("Byte value:"+a1);
	System.out.println("Int value:"+z1);

	//Byte into Long
	byte a2=8;
	long z2=a2;
	
	System.out.println("Byte value:"+a2);
	System.out.print("Long value:"+z2);
	
	//Byte into Float
	byte a3=5;
	float z3=a3;

	System.out.println(" Byte value:"+a3);
	System.out.println("Float value:"+z3);

	//Byte into Double
	byte a4=6;
	double z4=a4;
	
	System.out.println("Byte value:"+a4);
	System.out.println("Double value:"+z4);

	}
}
	