class converting
{
	public static void main(String[] args)
	{
	//Converting int into bytes,short,long,float,double,char 
	int room=2;
	
	//Converting int into byte
	byte age=(byte)room;
	System.out.println("Byte Value:"+age);
	
	//Converting int into short
	short hyy=(short)room;
	System.out.println("Short Value:"+hyy);
	
	//Converting int into long
	long yes=(long)room;
	System.out.println("Long Value:"+yes);
	
	//converting int into float
	float bye=(float)room;
	System.out.println("float Value:"+bye);

	//Converting int into double
	double hello=(double)room;
	System.out.println("Double Value:"+hello);
	
	//converting int into char
	char sin=(char)room;
	System.out.println("Char Value:"+sin);

	}
}
	